	@Test
	public void testGaussianKernelGeneration() {
		equalsMatrixValues(Convolution.generateDoubleGaussianKernel(3, 1.0), gaussian3x3kernel, EPSILON);
		equalsMatrixValues(Convolution.generateDoubleGaussianKernel(5, Math.sqrt(0.7)), gaussian5x5kernel, EPSILON);
		equalsMatrixValues(Convolution.generateDoubleGaussianKernel(11, Math.sqrt(0.7)), gaussian11x11kernel, EPSILON);
	}

	private static void equalsMatrixValues(final double[] m1, final double[] m2, final double epsilon) {
		assertTrue(m1.length == m2.length);
		for (int i = 0; i < m1.length; i++) {
			assertTrue("computeGodunovDifference: " + i + ", " + m1[i] + ", " + m2[i] + ", " + Math.abs(m1[i] - m2[i]),
					Math.abs(m1[i] - m2[i]) <= epsilon);
		}
	}
